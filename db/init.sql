-- Create workouts table (reference/template table)
CREATE TABLE IF NOT EXISTS workouts (
    id SERIAL PRIMARY KEY,
    name VARCHAR(255) NOT NULL,
    category VARCHAR(50) NOT NULL,  -- e.g., strength, cardio, flexibility
    logged_at TIMESTAMP DEFAULT NOW(),
    chest NUMERIC(2,1) DEFAULT 0,
    back NUMERIC(2,1) DEFAULT 0,
    shoulders NUMERIC(2,1) DEFAULT 0,
    biceps NUMERIC(2,1) DEFAULT 0,
    triceps NUMERIC(2,1) DEFAULT 0,
    quads NUMERIC(2,1) DEFAULT 0,
    hamstrings NUMERIC(2,1) DEFAULT 0,
    glutes NUMERIC(2,1) DEFAULT 0,
    calves NUMERIC(2,1) DEFAULT 0,
    abs NUMERIC(2,1) DEFAULT 0,
    forearms NUMERIC(2,1) DEFAULT 0,
    traps NUMERIC(2,1) DEFAULT 0,
    lats NUMERIC(2,1) DEFAULT 0,
    lower_back NUMERIC(2,1) DEFAULT 0,
    adductors NUMERIC(2,1) DEFAULT 0,
    abductors NUMERIC(2,1) DEFAULT 0
);

